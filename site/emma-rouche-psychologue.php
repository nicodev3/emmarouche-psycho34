        <h1>Qui suis-je&nbsp;?</h1>
<h2>Ma formation universitaire</h2>
<p>Originaire de Montpellier, j’y ai effectué mes études de psychologie 
jusqu’en Master 1. J’ai ensuite quitté la région pour terminer, l’année 
suivante, ma formation à l’Université de Paris Descartes où j’ai obtenu 
mon Master 2 en Psychologie clinique et psychopathologie. J’ai reçu tout
 au long de mon cursus universitaire une formation théorique 
d’orientation psychanalytique. De plus, j’ai suivi des modules 
spécifiques en psychologie projective (tests de personnalité) et en 
psychopathologie de l’adolescent et du jeune adulte. Par la suite, j’ai 
obtenu un Diplôme Universitaire de Psychologie Projective.</p>
<h2>Mon parcours</h2>
<p>Après mon Master 2, j’ai exercé plus de 5 années dans un Institut 
Thérapeutique Éducatif et Pédagogique (ITEP), implanté au coeur des 
quartiers sensibles de la Seine-Saint-Denis. J’y ai accompagné en 
psychothérapie des enfants, adolescents et jeunes adultes présentant 
d’importants troubles du comportement (agitation, violence, 
hyperactivité...) et des troubles des apprentissages (retard scolaire, 
dyslexie, dysorthographie...). J’ai également mené un groupe de parole, 
avec parents et famille des jeunes accueillis, afin que chacun puisse 
partager son expérience, dans un espace très convivial.</p>
<p>Enfin, dans le cadre du programme de Réussite Éducative de la ville 
de Massy, j’ai accompagné des enfants et des adolescents en difficultés 
psychologiques ainsi qu’en décrochage scolaire.</p><p>Aujourd'hui, en plus de ma pratique libérale, je poursuis mon travail auprès d'adolescents en difficulté dans un ITEP de l'Hérault.</p>
<h2>Mes spécialisations</h2>
<p>Mes compétences acquises à travers ces différentes expériences 
professionnelles m’ont amenée à me spécialiser en consultations pour 
enfants et adolescents. C’est ainsi que je propose la réalisation de 
bilans psychologiques ainsi que du soutien familial et parental. Par 
ailleurs, mon offre s’étend à des psychothérapies pour adultes.</p>
