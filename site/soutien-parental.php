                <h2>Le soutien parental&nbsp;:&nbsp;dans quelles situations&nbsp;?</h2>

<p>Le soutien parental et familial a pour objet d'accompagner les 
parents et familles dans leurs réflexions, leurs interrogations et leur 
positionnement.</p>











<p>Certaines étapes de la vie peuvent bouleverser la cellule familiale, 
la fratrie, le couple parental, et nécessiter quelques «accordages» :</p>



































<ul><li>l'arrivée d'un enfant</li><li>l'entrée à l'école</li><li>des difficultés scolaires</li><li>l'adolescence de son enfant</li><li>un déménagement</li><li>un divorce</li><li>famille monoparentale</li><li>la maladie, le handicap...</li></ul>

















<p>Face à ces situations, sont souvent éprouvés sentiments d'impuissance, de culpabilité, de rejet, d'incompréhension...</p>



















<p>Je vous propose un espace bienveillant, où vous pourrez vous exprimer
 sans aucun jugement et en toute liberté. Je pourrai également vous 
apporter un éclairage sur certaines situations.</p>





















<p>Ensemble, nous discuterons de vos difficultés pour vous aider à 
trouver vos propres solutions et faire émerger toutes vos ressources en 
tant que parent, individu et membre d'une famille.</p>
