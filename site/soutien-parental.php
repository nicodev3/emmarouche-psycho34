        <h1>Soutenir la famille et les parents</h1>

<p>Mon souhait de proposer du soutien parental et familial est né de mon
 expérience en institution médico-sociale, notamment de l’animation du 
«Café des Parents» et des consultations individuelles avec les parents 
ou en famille. Cette expérience humaine, d’une grande richesse, a été 
l’occasion de cibler précisément leurs besoins, et a mis en évidence la 
nécessité de créer un espace pour les accueillir.</p>

