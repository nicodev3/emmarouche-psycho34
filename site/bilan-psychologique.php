                <h2>Déroulement du bilan psychologique</h2>
<h3>Première consultation : la rencontre</h3>
<ul><li><p>Étude de la demande de la famille ou de l'institution scolaire et/ou médico-sociale.</p></li><li><p>Explication de la démarches et présentation des différentes étapes.</p></li></ul>
<h3>Deuxième et troisième consultations : la passation des tests </h3>
<p>L'enfant ou l'adolescent est reçu seul afin de passer les trois tests
 du bilan. Généralement, elle est organisée en deux temps : le test 
psychométrique et les tests de personnalité.</p>
<h3>Quatrième consultation : la restitution</h3>
<ul><li><p>Lors d'une dernière consultation, un compte-rendu complet du bilan est remis à la famille. Les résultats sont communiqués, détaillés et explicités au cours d'une lecture commune de cet écrit. </p></li><li><p>Je reste disponible pour répondre à vos éventuelles questions après cette dernière consultation.<br></p></li></ul>
