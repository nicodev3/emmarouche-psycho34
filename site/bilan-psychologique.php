        <h1>Qu'est-ce qu'un bilan psychologique&nbsp;?</h1>
<p>Le bilan psychologique permet de faire le point sur les compétences 
de l’enfant ou de l’adolescent, pour cerner d’une part ses difficultés 
et/ou ses aptitudes intellectuelles ; d’autre part, la façon dont il 
gère ses émotions et le rapport à l’autre.</p>
<p>Il comporte généralement trois «épreuves» : un test psychométrique (WICS V dit «Test de Q.I.») et deux tests de personnalité.</p>
<p>Dans certains cas, seul(s) le test psychométrique ou les tests de 
personnalité peuvent être effectués. Cela dépend de la situation et de 
la nature de la demande. Dans tous les cas, le psychologue prend le 
temps d’étudier la situation avec la famille et l’enfant ou adolescent ,
 afin de faire le point sur la nécessité ou non de passer un bilan et 
sur les attentes formulées.</p>
<p>Faire passer à votre enfant ou adolescent un bilan psychologique est 
une démarche conséquente. Il est important de réfléchir ensemble à votre
 demande afin de créer les conditions les plus favorables au bien-être 
de votre enfant au cours de ce moment.
</p>
    </header>
    
        
            <div class="has-img-border perch-paragraph">
                <h2>Test psychométrique - WISC V&nbsp; («Test du Q.i»)</h2>
<p>Plus connu sous le nom de «Test de Q.I», le bilan psychométrique est 
en réalité un examen complet qui permet d'appréhender le fonctionnement 
intellectuel d'un sujet, de repérer un éventuel retard, un haut potentiel, ou encore, un trouble des apprentissages.</p>
<p>Le test de Q.I est réalisé à l’aide des échelles d’intelligence de 
Wechsler pour enfants et adolescents, il peut être pratiqué dès l’âge de
 6 ans jusqu’à 16 ans 11 mois.</p>
<p>La passation de ce test peut s’étendre entre 1h30 et 3h. Elle peut 
également être effectuée en plusieurs séances, en fonction de l’état et 
du besoin du patient (difficulté de concentration, agitation...).</p>
<p class="redactor-important">Important : Le Bilan psychométrique est une mesure des compétences 
cognitives d’un patient à «l’instant T». C’est un peu comme une photo 
qui viendrait capturer une scène et la représenter à l’instant précis. 
Il est représentatif de la situation actuelle. Le quotient intellectuel 
est susceptible d’évoluer tout au long de la vie, il ne peut et ne doit 
pas être définitivement «figé» par la passation d’un bilan.</p>
            </div>
        
    
        
            <div class="has-img-border perch-paragraph">
                <h2>Tests de personnalité</h2>
<p>Les tests de personnalité sont des tests dits «projectifs». Les deux 
tests les plus fréquemment passés lors d’un bilan psychologique sont le 
test du Rorschach (test des tâches d’encre) et le TAT (Thematic 
Aperception Test) ou sa version pour les enfants (C.A.T).</p>
<p>Pour ces tests, il n’y a pas de bonnes ou mauvaises réponses. Chaque 
sujet va percevoir des choses différentes en fonction de sa 
personnalité.</p>
<p>Ils permettent de donner des indications sur la personnalité du 
patient, sa façon d'interagir avec l’environnement au sens large, les 
angoisses traversées, et tout ce qui est en lien avec la sphère 
affective.</p>
            </div>
        
    
        
