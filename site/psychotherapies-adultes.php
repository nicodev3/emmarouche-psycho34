        <p>S'engager dans un travail de psychothérapie ou de suivi psychologique, nécessite avant toute chose un lien de confiance avec son psychologue psychothérapeute.</p>

<p>Il est important de se sentir à l'aise, de pouvoir faire part de ses pensées, préoccupations et questionnements de façon aussi spontanée que possible, dans un cadre de confiance et de neutralité dénué de jugement.</p>



<p>Le temps d'une psychothérapie est un moment à soi, pour soi, pendant lequel on cherche à mettre du sens sur son mal-être, ses symptômes et les étapes douloureuses de sa vie. C'est une parenthèse intemporelle où l'on découvre peu à peu sa façon «d'être au monde», sa façon d'être en relation avec les gens qui nous entourent (vie sentimentale, vie familiale, vie professionnelle etc).</p>



<p>Le travail de psychothérapie est un «contrat» tacite mais primordial entre un patient dans le besoin de comprendre son vécu et sa souffrance, et un psychologue clinicien neutre et humain avant tout. C'est la rencontre de deux singularités qui vont parcourir ensemble, un bout de chemin.</p>
