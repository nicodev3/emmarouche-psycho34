        <h2>Ma pratique</h2>

<p>Je propose des psychothérapies d'orientation psychanalytique. Il 
s'agit d'une écoute permettant à la personne de mettre ses difficultés 
en lien avec son histoire passée et/ou son quotidien, afin de donner un 
sens à sa souffrance et de pouvoir la dépasser. La réflexion du patient 
est privilégiée et le psychologue intervient pour l'aider à prendre 
conscience d'un certains nombre de choses et à faire des liens. Ces 
interventions se font en fonction de son besoin d'étayage et de soutien.
 Le patient est pleinement acteur de sa vie, de son travail de 
psychothérapie et de son désir de comprendre afin d'évoluer.</p>
    </header>
    
        
            <div class="has-img-border perch-paragraph">
                <h2>La mise en place d'un suivi : soutien psychologique et psychothérapies</h2>

<p>La démarche vers un psychologue s'effectue généralement dans une 
période de changements importants, de questionnements et/ou de 
fragilité. Vous traversez un passage difficile dans votre vie amoureuse,
 familiale ou professionnelle ; vous vous sentez inquiet, préoccupé, 
avez perdu confiance en vous, ne trouvez plus le sommeil, êtes anxieux&hellip;</p>









<p>Au cours d'une première rencontre, nous prendrons le temps de faire 
connaissance. Vous me ferez part de votre situation, de ce qui vous a 
conduit à prendre rendez-vous. En fonction de vos attentes et de ce que 
je pourrai vous proposer, nous déciderons ensemble du travail à engager.</p>







<h3>Le soutien psychologique</h3>









<p>C'est une aide ponctuelle. Il arrive de solliciter un psychologue de 
façon temporaire, dans un contexte bien précis. Cet accompagnement peut 
aller de quelques semaines à quelques mois, à raison d'un rendez-vous 
par semaine ou d'une rendez-vous toutes les quinze jours.</p>







<h3>La psychothérapie</h3>









<p>Elle s'adresse aux personnes rencontrant des difficultés 
psychologiques liées à des évènements de vie douloureux ou souhaitant 
engager un travail sur elles-mêmes afin de mieux se comprendre. Il 
s'agit d'un accompagnement plus soutenu, généralement au rythme d'une 
séance par semaine, ou d'un rendez-vous bi-mensuel. Ce travail peut 
aller de quelques mois à quelques années.</p>
